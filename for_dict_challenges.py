# Есть список учеников в нескольких классах, нужно вывести самое частое имя в каждом классе.
school_students = [
  [  # это – первый класс
    {'first_name': 'Вася'},
    {'first_name': 'Вася'},
  ],
  [  # это – второй класс
    {'first_name': 'Маша'},
    {'first_name': 'Маша'},
    {'first_name': 'Оля'},
  ]
]

list_classes = []

for clas in school_students:
    counter = {}
    for student in clas:
        if student['first_name'] in counter:
            counter[student['first_name']] += 1
        else:
            counter[student['first_name']] = 1
    print(counter)
    list_classes.append(counter)
    print(list_classes)

for c in list_classes:
    print(f"The most frequent name in class {list_classes.index(c)+1}: {max(c, key=c.get)}")

