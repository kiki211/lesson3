import operator
# Задание 1
# Дан список учеников, нужно посчитать количество повторений каждого имени ученика.
students = [
  {'first_name': 'Вася'},
  {'first_name': 'Петя'},
  {'first_name': 'Маша'},
  {'first_name': 'Маша'},
  {'first_name': 'Петя'},
]
rep_statistics = {}
for student in students:
  for key, value in student.items():
    if value not in rep_statistics:
      rep_statistics[value] = 1
    else:
      rep_statistics[value] += 1
for key, value in rep_statistics.items():
  print(key + ":" + str(value))





# Пример вывода:
